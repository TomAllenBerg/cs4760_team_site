										  <h3>Application Background</h3>
										  <p>Large buildings must be routinely inspected in order to ensure safety. Specifically, buildings such as sports arenas must have the ceiling trusses inspected. Each "joint/node" where the truss meets must be visually inspected for defects. Traditionally, this has been an arduous process, done by using scissor lifts and continually going up/down many times in order to cover the entire space. This obviously is very inefficient and slow. Due to the difficult nature of the task, it was not uncommon for these inspections to be skipped altogether. With the advent of modern drone technology, the task of inspecting large buildings has been streamlined.
										    <br/><br/>
										    With that being said, flying drones is a difficult task. It is very much subject to human error and crashes may occur frequently when flying in difficult or unfamiliar environments. Additionally, in indoors environments there is little or no GPS signal, and so flights must be manual. Given that flying in unfamiliar indoor environments is so difficult, it would be beneficial to provide some medium for training and familiarizing novice pilots to fly in a particular space before making the flight on-site. That is, to familiarize and train people to repeat or perform a similar flight path to that of a path taken during an inspection by an expert drone pilot.
										  </p>
										</div><br>
										<div>
											 <h3>The Application</h3>
											 <p>This app seeks to provide a web-based method of visualizing drone flights used in interior building inspections, or wherever autonomous GPS-driven flights are not feasible. This visualization will enable drone pilots to better understand the flight paths of professional pilots who have already conducted simulated inspections within the particular space. Delivering this functionality is contingent on communicating spatial flight path data intuitively within a 3D space.
